package com.example.kalkulator;

public class Data {
    String a;
    boolean b;
    float f;
    double d;

    public Data(String a, boolean b, float f) {
        this.a = a;
        this.b = b;
        this.f = f;
    }

    public String getA() {
        return a;
    }

    public boolean isB() {
        return b;
    }

    public float getF() {
        return f;
    }

    public double getD() {
        return d;
    }
}
